    public void setCbxMaSP(){
        List<String> list=service.getMaSP();
        for(String x : list){
            cbxMaSP.addElement(x);
        }
        cbxMa.setModel(cbxMaSP);
    }
    
    public void setCbxTheLoai(){
        List<String> list = service.cbxTheLoai();
        for (String x : list) {
            cbx.addElement(x);
        }
        cbxTheLoai.setModel(cbx);
        
    }
    
    public void setCbxTheTich(){
        List<String> list = service.cbxDungTich();
        for (String x : list) {
            cbxTheTich1.addElement(x);
        }
        cbxTheTich.setModel(cbxTheTich1);
        
    }
    public void fillTable(List<SanPham> sp){
        model=(DefaultTableModel) bang.getModel();
        model.setRowCount(0);
        for(SanPham tam:sp){
            model.addRow(new Object[]{tam.getMact(),tam.getMasp(),tam.getTensp(),tam.getSoluong(),tam.getGia(),tam.getKetcau(),tam.getIdTheTich(),tam.getIdTheLoai() ,tam.getMota()});
        }
    }
    public SanPham readform(){
        SanPham sp=new SanPham();  
        sp.setSoluong(Integer.parseInt(txtSL.getText()));
        sp.setGia(Float.parseFloat(txtGia.getText()));
        sp.setKetcau(txtKC.getText());
        sp.setIdTheLoai(service.getIdTheLoai(cbxTheLoai.getSelectedItem()+""));
        sp.setIdTheTich(service.getIdTheTich(cbxTheTich.getSelectedItem()+""));
        sp.setMasp(cbxMa.getSelectedItem()+"");
        return sp;
    }
    public SanPham readformSP(){
        SanPham sp=new SanPham();
        sp.setMasp(txtMa.getText());
        sp.setTensp(txtTen.getText());
        sp.setMota(txtMT.getText());
        return sp;
    }
    
    public void showDetail(int index){
        SanPham sp=service.getAll().get(index);
        txtMa.setText(sp.getMasp());
        txtTen.setText(sp.getTensp());
        txtGia.setText(sp.getGia()+"");
        txtSL.setText(sp.getSoluong()+"");
        txtMT.setText(sp.getMota());
        txtKC.setText(sp.getKetcau());
        cbxMa.setSelectedItem(sp.getMasp()+"");
        cbxTheLoai.setSelectedItem(sp.getIdTheLoai()+"");
        cbxTheTich.setSelectedItem(sp.getIdTheTich()+"");
    }
